public class Classe{
    Integer idClasse;
    String designation;

    public Integer getIdClasse() {
        return idClasse;
    }
    public void setIdClasse(Integer idClasse) {
        this.idClasse = idClasse;
    }
    public String getDesignation() {
        return designation;
    }
    public void setDesignation(String designation) {
        this.designation = designation;
    }
}